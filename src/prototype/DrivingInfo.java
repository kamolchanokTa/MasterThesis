package prototype;

public class DrivingInfo {
	
	public double x;
	public double y;
	public double speed;
	public double meetingLocation;
	public DrivingInfo(double x, double y, double speed, double meetingLocation) {
		this.x = x;
		this.y= y;
		this.speed = speed;
		this.meetingLocation = meetingLocation;
	}
}
